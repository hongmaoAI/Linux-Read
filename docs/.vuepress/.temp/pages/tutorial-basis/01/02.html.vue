<h3 id="选择合适你的-linux-distribution" tabindex="-1"><a class="header-anchor" href="#选择合适你的-linux-distribution"><span>选择合适你的 Linux distribution</span></a></h3>
<p>上面所述主差异性并不大，鸟哥建议是先学习以 RPM 套件管理为主的 RHEL/Fedora/SuSE/centOs
等中国用户较多的版本</p>
<p>总之版本很多，但是差异并不是很大，选定一个版本从头彻尾的了解后，再去学习其他版本就很容易了，
如果提供一些按计算机用途来分类的建议：</p>
<ul>
<li>
<p>企业环境：选择商业版本，如 RedHat 的 RHEL 或则是 Novel 的 SuSE。因为会提供客服服务，降低企业风险</p>
</li>
<li>
<p>个人或教学服务器环境：建议使用号称完全兼容商业版本 RHEL 的 CentOS</p>
<p>因为 CentOS 是抓 RHEL 的源码重新都起来一个 linux distribution，这一版本的软件与 RHEL 完全一样，
改版幅度较小，适合用于服务器系统的场景</p>
</li>
<li>
<p>个人桌面计算机</p>
<p>想要尝鲜的话可以使用 Fedora、Ubuntu 等 Desktop（桌面环境）使用版本</p>
</li>
</ul>
