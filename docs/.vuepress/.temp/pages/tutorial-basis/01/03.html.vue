<h2 id="关于授权" tabindex="-1"><a class="header-anchor" href="#关于授权"><span>关于授权</span></a></h2>
<p>市面上有好多软件，有自由软件，有专利软件，有的专利软件免费，有的自由软件要钱，难分辨。
这里有几个授权模式可以来谈一谈</p>
<ul>
<li>
<p>Open Source（开放源代码）</p>
<p>开源软件发布时，除了可执行软件本身，一定伴随着源码的释出。
开源原件的代表授权为 GNU 的 GPL 授权及 BSD 等，下面列出来</p>
<ul>
<li>
<p>GUN General Public License</p>
<p>目前有 version 2、version 3 两种版本，Linux 使用的是 Version 2</p>
</li>
<li>
<p>Berkeley Software Distribution（BSD）</p>
<p>与 GPL 很类似</p>
</li>
<li>
<p>Apache License Version 2.0</p>
<p>Apache 是一种网页服务器软件，也是开源的，但是授权中规定，如果想要重新发布此软件时
（如果你有修改过该软件），软件的名称依旧需要定名为 Apache 才行</p>
</li>
</ul>
</li>
<li>
<p>Close Source</p>
<p>没有源码的释出，这种软件一般有专人维护，你无法更改该程序为自己想要的样式。
另外由于不是开源的，出现木马程序或则安全漏洞，将会画上相当长的一段时间来排错，
这也是所谓商业软件（Copyright）常见的软件出售方式</p>
<p>还有一种免费的商业软件代表授权模式：Feweeare ，不同于 Free Software，
Feweeare 为 免费软件而非自由软件</p>
<p>Shareware 共享软件，有试用期，过了试用期就需要收费。</p>
</li>
</ul>
