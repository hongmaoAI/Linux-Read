<p>你知道你平时接触的电子商品中，哪些里面有 linux  系统存在呢？其实相当多，下面来谈一谈</p>
<h3 id="桌面计算机" tabindex="-1"><a class="header-anchor" href="#桌面计算机"><span>桌面计算机</span></a></h3>
<p>所谓桌面计算机，就是平时你我在办公室使用的计算机了。一般称为桌面系统</p>
<p>早期的 linux 是由工程师所发展得，所以对于窗口界面的设计并没有很需要，
后来 linux 与 x window System 结合了，要注意的是，x window system 只是 linux 上的一套软件，
而不是内核，所以即使 x window 挂了，对 linux 也可能不会有直接的影响</p>
<h3 id="手持系统-pda、手机" tabindex="-1"><a class="header-anchor" href="#手持系统-pda、手机"><span>手持系统（PDA、手机）</span></a></h3>
<p>很多的手机、pda、导航系统都可能使用的是 linux 操作系统，为了加强 linux 操作系统在手机上面的统一标准，
很多国际厂商合作了一个 LiMo 的计划（linux Mobile phone）.</p>
<h3 id="嵌入式系统" tabindex="-1"><a class="header-anchor" href="#嵌入式系统"><span>嵌入式系统</span></a></h3>
