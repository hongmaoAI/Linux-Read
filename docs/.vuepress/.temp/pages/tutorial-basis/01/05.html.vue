其他微型的计算机配备也是硬件系统。这些计算机配备也都是需要操作系统来控制的，而操作系统是直接嵌入产品当中的，
理论上你不应该更改到这个操作系统，所以就称为 <strong>嵌入式系统</strong></p>
<p>包括路由器、防火墙、手机、pda、交换机、家电产品的微电脑控制器等，都可以是 linux 操作系统</p>
