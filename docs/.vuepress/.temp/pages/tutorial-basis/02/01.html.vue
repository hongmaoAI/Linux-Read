<p>选购新的个人计算机时，需要注意某些硬件是否支持 linux，因为有些厂商对 linux 的驱动程序支持较慢</p>
<p>此外你需要用 linux 达成什么任务？也就是用途是什么？这样才知道哪个硬件才是最重要的。
举例来说，桌面电脑主要使用 x window，那么对于内存和显卡就显得比较重要了，
想要做成文件服务器，那么硬盘或则是其他存储设备比较重要</p>
<div class="hint-container tip">
<p class="hint-container-title">提示</p>
<p>再次强调，linux 对硬件设备的分辨与 windows 不同，在 linux 下面都是「文件」，
在学习 linux 设备文件名之前，务必要先将 windows 中的设备概念先拿掉</p>
</div>
<h2 id="认识计算机的硬件配备" tabindex="-1"><a class="header-anchor" href="#认识计算机的硬件配备"><span>认识计算机的硬件配备</span></a></h2>
<p>主要部件有：</p>
<ul>
<li>CPU</li>
<li>内存</li>
