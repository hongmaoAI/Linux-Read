<li>硬盘</li>
<li>主板</li>
</ul>
<h3 id="游戏机-工作机的考虑" tabindex="-1"><a class="header-anchor" href="#游戏机-工作机的考虑"><span>游戏机/工作机的考虑</span></a></h3>
<p>对于游戏机来说，更高的 CPU、内存、显卡是必须的，
但是对于一般的办公室环境来说，入门级的计算机就行了，基本用来制作 ppt、word、网页浏览等工作</p>
<h3 id="性能-价格比性能-消耗的瓦数考虑" tabindex="-1"><a class="header-anchor" href="#性能-价格比性能-消耗的瓦数考虑"><span>性能/价格比性能/消耗的瓦数考虑</span></a></h3>
<p>买最新的硬件一般来说有可能 linux 下没有合适的驱动程序，而且价格贵。</p>
<p>另外耗电量也需要考虑，特别是在企业环境中，有机房集群什么的使用计算机的数量是很多的，
硬件评论界有所谓的「每瓦性能」，就是每瓦电力发挥的性能越高，就越省电，
比如做服务器用时，你的计算机比省电的计算机每天多花费 50 瓦电力时，50W<em>365</em>24小时/1000W = 438 度电，
以一度电 3 块钱来计算，就多花费十万电费</p>
<h3 id="支持度的考虑" tabindex="-1"><a class="header-anchor" href="#支持度的考虑"><span>支持度的考虑</span></a></h3>
<p>某些厂商没有意愿提供适当的驱动程序的话，那么你买了也在 linux 上运行不起来的</p>
<h2 id="选择与-linux-搭配的主机配备" tabindex="-1"><a class="header-anchor" href="#选择与-linux-搭配的主机配备"><span>选择与 Linux 搭配的主机配备</span></a></h2>
<p>由于硬件的加速发展与操作系统核心功能的增强，导致老旧的硬件在新的内核上支持不好，可能导致不明的死机状况，
所以选择与之匹配的硬件与操作系统版本是很有必要的</p>
<p>由于 linux 对硬件的要求不高，鸟哥的建议是，如果你有 5 年内的计算机被淘汰的话，可以先尝试一下，
说不定就能作为你日常生活的 linux 服务器或则是备用服务器</p>
<p>下面来稍微谈一下，如果你的需求是将 linux 作为小型服务器使用，不负责学术方面的大量运算，
也没有 x window，那么你的硬件需求只要像下面这样就够了</p>
<ul>
<li>
<p>CPU：2015 年中，intel i3 系列的 CPU 就可以了</p>
</li>
<li>
<p>RAM: 当然是越大越好</p>
<p>在 Linux 服务器中，内存的重要性比 CPU 还要高的多，内存不够就会用到交换空间（swap），
而交换空间是使用硬盘存储的，硬盘比内存速度慢太多</p>
<p>至少 512 MB，在 DDR3 的硬件环境中，4~16 GB 的内存够用了</p>
</li>
<li>
<p>Hard Disk</p>
<p>主要看你用来做什么，如果是小型服务器，一般容量大于 20 GB，就很够用了，如果是用来作为备份
或小企业的文件服务器，那么可能就得需要磁盘阵列（RAID)模式了（Raid 利用硬件技术将多个硬盘整合为一个大硬盘的方法）</p>
</li>
<li>
<p>VGA</p>
<p>不需要 x window 的话，只要最基础的显卡能让计算机启动就行了，否则至少拥有 32 MB以上的显存容量，
不然 x window 可能会很卡</p>
</li>
<li>
<p>Network Interface Card</p>
<p>网卡是服务器上面最重要的元件之一了。大多主板内置 10/100/1000 Mbps 的超高速显卡。
但是有不同的差异，比如具有设置 bonding 功能的网卡，较好的网卡驱动也做得比较好，
如果是网络 I/O 频繁的需求，那么可以选用 intel/boradcom 等公司的网卡</p>
</li>
<li>
<p>光盘、软盘、键盘与鼠标</p>
<p>基本上可以不准备了，一般安装好之后，都会拔掉，通过终端来管理</p>
</li>
</ul>
<h3 id="小型主机且不含-x-window-系统硬件推荐" tabindex="-1"><a class="header-anchor" href="#小型主机且不含-x-window-系统硬件推荐"><span>小型主机且不含 x window 系统硬件推荐</span></a></h3>
<p>用途：家庭用 NAT 主机（IP 分享器功能）或小型企业之非图形接口小型主机</p>
<ul>
<li>CPU：五年内出产的产品即可</li>
<li>RAM：至少 512 MB，大于 1 GB 最好</li>
<li>网卡：一般的以太网卡即可应付</li>
<li>显卡：只要能被 Linux 捉到的显卡即可，例如 Nvidia 或 Ati 的主流显卡</li>
<li>硬盘：20 GB 以上即可</li>
</ul>
<h3 id="桌上型含-x-window-系统" tabindex="-1"><a class="header-anchor" href="#桌上型含-x-window-系统"><span>桌上型含 x window 系统</span></a></h3>
<p>用途：Linux 的练习机或办公室（Office）工作机（一般我们会用到的环境）</p>
<ul>
<li>CPU：最好等级高一点，例如 Intel I5, I7 以上等级。</li>
<li>RAM：一定要大于 1GB 比较好!否则容易有图形接口停顿的现象。</li>
<li>网卡：普通的以太网卡就好了!</li>
<li>显卡：使用 256 MB 以上内存的显卡!(入门级的都这个容量以上了)</li>
<li>硬盘：越大越好，最好有 60 GB。</li>
</ul>
<h3 id="中型以上-linux-服务器" tabindex="-1"><a class="header-anchor" href="#中型以上-linux-服务器"><span>中型以上 Linux 服务器</span></a></h3>
<p>用途：中小型企业/学校单位的 FTP、mail、wwww 等网络服务主机</p>
<ul>
<li>CPU：最好等级高一点，例如 I5, I7 以上的多核心系统。</li>
<li>RAM: 最好能够大于 1GB 以上，大于 4GB 更好!</li>
<li>网卡: 知名的 broadcom 或 Intel 等厂牌，比较稳定性能较佳!</li>
<li>显卡: 如果有使用到图形功能，则一张 64 MB 内存的显卡是需要的!</li>
<li>硬盘: 越大越好，如果可能的话，使用磁盘阵列，或者网络硬盘等等的系统架构， 能够具有更稳定安全的传输环境，更 佳!</li>
</ul>
<p>建议企业用计算机不要自行组装，可购买商用服务器较佳， 因为商用服务器已经通过制造商的散热、稳定性等测试，对 于企业来说，会是一个比较好的选择。</p>
<p>总之这里提供一个方向，根据自己的需求选购。</p>
