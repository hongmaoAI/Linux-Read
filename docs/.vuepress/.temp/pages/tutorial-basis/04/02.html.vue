<p>有时候，还会看到 authors 与 Copyright 等，不过有很多时候仅有 NAME 与 DESCRIPTION 等部分。
通常可以这样来查询</p>
<ol>
<li>
<p>先看 NAME 部分，略看下这个资料的意思</p>
</li>
<li>
<p>再详看 DESCRIPTION，这部分会提到很多相关的资料与使用时机，可以学到很多小细节</p>
</li>
<li>
<p>如果这个指令其实你很熟悉了（例如上面的 date），就可以主要查询下 OPTIONS 部分了，可以知道每个选项的含义</p>
</li>
<li>
<p>最后，可以看下跟这个资料有关的还有哪些可以使用</p>
<p>例如上面就告知我们还可以利用 <code v-pre>info coreutils date</code> 指令来进一步查阅</p>
</li>
<li>
<p>最后可以看下 FILES 部分</p>
</li>
</ol>
