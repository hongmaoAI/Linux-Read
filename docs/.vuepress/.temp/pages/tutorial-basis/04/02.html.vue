<h2 id="info-page" tabindex="-1"><a class="header-anchor" href="#info-page"><span>info page</span></a></h2>
<p>在所有的 unix like 系统中都可以使用 man 来查询指令或则是相关文件的用法。在 linux 中又提供了一种 info page</p>
<p>基本上 info 与 man 的用途其实差不多，但是是一个输出显示比较好的排版，
info page 将文件数据拆成一个一个的段落，每个段落用一个页面展示，各个页面中还有类似网页的超链接来跳转到不同的页面中，每个独立的页面也称为一个节点（node）。可以想象成 info page 是文本模式的网页显示数据</p>
<p>info 指令的文件默认是放在 <code v-pre>/usr/share/info</code> 目录下的</p>
<p><img src="@source/tutorial-basis/04/assets/markdown-img-paste-20191005231738416.png" alt=""></p>
<p>第一行数据含义：</p>
<ul>
<li>file ：表示这个文档的资料来自 info.info 文件提供的</li>
<li>Node：这个页面属于 top 节点。info.info 内含有很多信息，top 只是其中一个节点内容</li>
<li>Next：下一个节点的名称是 Getting Started，可以使用「N」按键到下个节点去</li>
<li>Up：使用 「U」回到上一层</li>
<li>Prev：前一个节点。</li>
</ul>
<p>还有一个按键，「h」会进入简易按钮说明，上面列出来的跳转到上下节点，就是在这个里面有说明</p>
