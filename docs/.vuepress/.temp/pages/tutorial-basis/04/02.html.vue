<h2 id="其他有用的文件-documents" tabindex="-1"><a class="header-anchor" href="#其他有用的文件-documents"><span>其他有用的文件（documents）</span></a></h2>
<p>一般指令或软件制作者，都会将软件说明制作成「联机帮助文件」，但是不一定都需要，可能还有额外更多的说明文件，
而且有些软件不只会告诉你如何做，还会告诉你一些相关的原理</p>
<p>/usr/share/doc 这个目录下一般会放置这些说明文件，比如之前分区讲 grub2 开机管理软件有什么能使用的指令？
那么久可以到 <code v-pre>/usr/share/doc/grub2-tools-2.02</code>(笔者看到的就不是这个，但是的确有相关的)</p>
<p>该目录下的数据主要是以套件（packages）为主，比如 nano 这个软件的相关信息就在 <code v-pre>/usr/share/doc/nano-xx</code>(xx 表示软件版本)</p>
<h2 id="总结" tabindex="-1"><a class="header-anchor" href="#总结"><span>总结</span></a></h2>
<ul>
<li>在终端机模式中，如果你知道某个指令，但是忘记了相关选项与参数，善用 --help 的功能来查询相关信息</li>
<li>当有任何你不知道的指令或文件格式，但是你又想了解他，请使用 man 或则是 info</li>
<li>如果你想要架设一些其他服务，或要利用一整组软件来达成某项功能时，请到 /usr/share/doc 查询看有没有相关说明</li>
</ul>
<p>另外，这些文档中文文件很少，但是不要怕，翻译查阅把</p>
