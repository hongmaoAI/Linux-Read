<h3 id="man-page-文档路径" tabindex="-1"><a class="header-anchor" href="#man-page-文档路径"><span>man page 文档路径</span></a></h3>
<p>man page 搜索的不是在线的（前面书上说是在线求助），提供 man page 的数据通常放在 <code v-pre>/usr/share/man</code>
目录的。</p>
<p>可以修改 <code v-pre>/etc/man_db.conf</code>（由于差异性可能是 man.conf、manpath.conf、man.config 等）中参数
来修改 man page 提供数据的目录地址.</p>
<p>更详细的后面 bash 会讲解</p>
