<template><div><h1 id="vi-的使用" tabindex="-1"><a class="header-anchor" href="#vi-的使用"><span>vi 的使用</span></a></h1>
<p>基本上 vi 共分为三种模式：一般指令模式、编辑模式、指令列命令模式</p>
<ul>
<li>
<p>一般指令模式（command mode）</p>
<p>以 vi 打开一个文件就直接进入一般指令模式了（默认模式，也简称一般模式）。</p>
<p>在该模式中，可以使用「上下左右」按键移动光标，可以使用「删除字符」或「删除整列」来处理文件内容，也可以使用「复制、粘贴」</p>
</li>
<li>
<p>编辑模式（insert mode）</p>
<p>在一般模式中可以进行删除、复制、粘贴等动作，但是无法编辑文件内容。</p>
<p>需要按下「i、I、o、O、a、A、r、R」等任意按键后才会进入编辑模式，通常会在左下方出现 INSERT 或 REPLACE 的字样，可以通过 esc 按键退出编辑模式，回到一般指令模式</p>
</li>
<li>
<p>指令列命令模式（commadn-line mode）</p>
<p>在一般模式中，输入「:、/、?」任意字符，则光标会移动到最底下的一列。</p>
<p>在这个模式中，可以提供你搜索、读取、存盘、大量取代字符、离开 vi、显示行号等功能</p>
</li>
</ul>
