<li>与使用者创建、更改参数、删除有关的指令为：useradd、usermod、userdel 等，passwd 是创建密码</li>
<li>与群组创建、修改、删除的有关指令为：groupadd、groupmod、groupdel 等</li>
<li>群组的观察与有效群组的切换为：groups 、newgrp</li>
