<p>备份就是为了防止系统挂掉，如果系统真的挂掉，你该如何还原系统？</p>
<ul>
<li>
<p>硬件损坏，且具有完整备份的数据时</p>
<p>由于是硬件损坏，需要先将硬件搞定，然后直接将完整的系统还原回去即可</p>
</li>
<li>
<p>由于软件问题产生的被黑客攻击事件</p>
<p>由于系统的损坏是因为被攻击，此时即使你恢复到正常的系统，那么这个系统任然可能被再次攻击，所以此时完成备份的复原可能不是一个好的方式，最好如下处理：</p>
<ol>
<li>先拔出网线，将系统完整备份到其他设备上，以备未来查验</li>
<li>开始查询日志文件，尝试找出各种可能的问题</li>
<li>开始安装新系统（最好找最新的 distribution）</li>
<li>进行系统的升级，与防火墙相关机制的设置</li>
<li>根据 2 的错误，在安装完新系统后，将那些 bug 修复</li>
<li>进行各项服务于相关数据的恢复</li>
<li>正式上线提供服务，并且开始测试</li>
</ol>
<p>发生被攻击事件后，这个问题可大可小，一般来说，标准流程都是建议将出问题的系统备份下来，如果被追踪到你的主机曾经攻击过别人的话，那么你至少可以拿出备份数据来佐证说，你是被攻击者，而不是主动攻击别人的坏人</p>
</li>
</ul>
